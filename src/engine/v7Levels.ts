const repeat = (token: MoveToken, n: number): MoveToken[] => Array.from({ length: n }, () => token);
const spaces = (n: number): string => ' '.repeat(Math.max(0, n));

function route(pattern: string): MoveToken[] {
  const table: Record<string, MoveToken> = {
    U: 'up',
    D: 'down',
    L: 'left',
    R: 'right',
  };
  const tokens: MoveToken[] = [];
  for (let i = 0; i < pattern.length; i++) {
    const ch = pattern[i]!;
    if (ch === ' ' || ch === ',' || ch === '-') continue;
    const t = table[ch];
    if (!t) throw new Error(`Unknown route token '${ch}' in ${pattern}`);
    tokens.push(t);
  }
  return tokens;
}

function v7(input: V7Input): V7LevelDef {
  const {
    coreIdea,
    trick,
    fairness,
    difficulty,
    ...def
  } = input;
  return {
    ...def,
    levelDesignNote: {
      id: def.id,
      title: def.name,
      chapter: def.chapter,
      mechanics: def.mechanics,
      coreIdea,
      trick,
      fairness,
      difficulty,
      solverStatus: def.solverStatus,
      par: def.par,
      solution: def.solution,
    },
  };
}

function corridor(input: LineInput): V7LevelDef {
  const axis = input.axis ?? 'h';
  const solution =
    axis === 'h'
      ? repeat('right', input.pre + input.pushes)
      : repeat('down', input.pre + input.pushes);

  if (axis === 'h') {
    const row = `#@${spaces(input.pre)}$${spaces(input.pushes - 1)}.#`;
    return v7({
      ...input,
      par: solution.length,
      map: ['#'.repeat(row.length), row, '#'.repeat(row.length)],
      solution,
    });
  }

  const rows = ['###', '#@#'];
  for (let i = 0; i < input.pre; i++) rows.push('# #');
  rows.push('#$#');
  for (let i = 0; i < input.pushes - 1; i++) rows.push('# #');
  rows.push('#.#', '###');
  return v7({ ...input, par: solution.length, map: rows, solution });
}

function portalFold(input: Omit<LineInput, 'axis' | 'mechanics' | 'spaceProfile'> & {
  gap: number;
  mechanics?: V7Mechanic[];
  spaceProfile?: SpaceProfile;
}): V7LevelDef {
  const width = input.gap + 7;
  const top: string[] = Array.from({ length: width }, (_, i) => (i === 0 || i === width - 1 ? '#' : ' '));
  const bottom: string[] = Array.from({ length: width }, (_, i) => (i === 0 || i === width - 1 ? '#' : ' '));
  top[1] = '@';
  top[2 + input.gap] = 'o';
  bottom[2] = '.';
  bottom[3 + input.gap] = '$';
  bottom[4 + input.gap] = 'o';
  const solution = [...repeat('right', input.gap + 1), ...repeat('left', input.gap + 1)];
  return v7({
    ...input,
    mechanics: input.mechanics ?? ['quantum-portal'],
    spaceProfile: input.spaceProfile ?? 'portal-linked',
    par: solution.length,
    map: ['#'.repeat(width), top.join(''), '#'.repeat(width), bottom.join(''), '#'.repeat(width)],
    solution,
  });
}

function syncLine(input: Omit<LineInput, 'axis' | 'mechanics' | 'spaceProfile'> & {
  mirrorTwin?: boolean;
  mechanics?: V7Mechanic[];
  spaceProfile?: SpaceProfile;
}): V7LevelDef {
  const row = `#@${spaces(input.pre)}$${spaces(input.pushes - 1)}.#`;
  const twinRow = input.mirrorTwin
    ? row.split('').reverse().join('')
    : row;
  const solution = repeat('right', input.pre + input.pushes);
  return v7({
    ...input,
    mechanics: input.mechanics ?? (input.mirrorTwin ? ['sync-actors', 'mirror-field'] : ['sync-actors']),
    spaceProfile: input.spaceProfile ?? (input.mirrorTwin ? 'symmetric' : 'dual-room'),
    par: solution.length,
    map: ['#'.repeat(row.length), row, '#'.repeat(row.length)],
    twin: ['#'.repeat(twinRow.length), twinRow, '#'.repeat(twinRow.length)],
    solution,
  });
}

