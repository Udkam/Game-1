type V7Input = Omit<V7LevelDef, 'levelDesignNote'> & {
  coreIdea: string;
  trick: string;
  fairness: string;
  difficulty: 1 | 2 | 3 | 4 | 5;
};

type LineInput = Omit<V7Input, 'map' | 'solution' | 'par'> & {
  axis?: 'h' | 'v';
  pre: number;
  pushes: number;
};

